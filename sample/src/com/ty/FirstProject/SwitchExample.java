package com.ty.FirstProject;

public class SwitchExample {
	public static void main(String[] args) {
		
		int n=4;
		switch (n) 
	{
			
		case 1:
		    System.out.println("10");
			break;
		case 2:
			System.out.println("20");
			break;
		case 3:
			System.out.println("30");
			break;
		
		default :
		System.out.println("Not 10,20,30");
	}

}
}
